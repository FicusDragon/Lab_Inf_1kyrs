#include <iostream>
#include <cmath>

int main() {
    int M = 0; 
    int step = 1; 
    int max_value = 10; 
    float A = 2.5;
    double Y;

    // цикл for
    for (int i = 0; i <= max_value; i += step) {
        M = i;
        std::cout << "Значение переменной: " << M << std::endl;
        Y = exp(abs(A + M));
        std::cout << "Результат: " << Y << std::endl;
    }

    return 0;
}
